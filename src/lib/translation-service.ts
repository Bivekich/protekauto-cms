/**
 * Переводит название категории с английского на русский
 */
export const translateCategoryName = (englishName: string): string => {
  const translation = PARTSAPI_TRANSLATIONS[englishName];
  if (translation) {
    console.log(`🌐 Переводим "${englishName}" → "${translation}"`);
    return translation;
  }
  console.log(`⚠️ Перевод не найден для "${englishName}", оставляем как есть`);
  return englishName;
};

/**
 * Переводит массив названий категорий
 */
export const translateCategoryNames = (englishNames: string[]): string[] => {
  return englishNames.map(translateCategoryName);
};

/**
 * Проверяет, есть ли перевод для данного названия
 */
export const hasTranslation = (englishName: string): boolean => {
  return englishName in PARTSAPI_TRANSLATIONS;
};

/**
 * Получает статистику переводов
 */
export const getTranslationStats = () => {
  const totalTranslations = Object.keys(PARTSAPI_TRANSLATIONS).length;
  console.log(`📊 Доступно переводов: ${totalTranslations}`);
  return {
    totalTranslations,
    categories: Object.keys(PARTSAPI_TRANSLATIONS)
  };
}; 